/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */


  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */

  TIM1_DMA_LED_Init();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  float heading, roll, pitch;

  static uint16_t lastled = LED_COUNT - 1;

  bno055_init();

  while (1)
  {
      bno055_read_euler(&heading, &roll, &pitch);

      uint16_t led = (uint16_t)(144.0f * heading / 360.0f);
      ws2812_set_rgb(lastled,0,0,0);
      ws2812_set_rgb(led, 0, 255, 0);
      lastled=led;
      ws2812_start();

      LL_mDelay(10);
  }




  static uint16_t hue_offset = 0;

  while (1)
  {
      LL_mDelay(3);   // скорость вращения радуги

      for (uint16_t i = 0; i < LED_COUNT; i++)
      {
          uint16_t hue = (i * 256 / LED_COUNT + hue_offset) & 0xFF;

          uint8_t r, g, b;
          hsv_to_rgb(hue, 255, 80, &r, &g, &b); // яркость 80 — щадит глаза

          ws2812_set_rgb(i, r, g, b);
      }

      ws2812_start();

      hue_offset++;   // вращаем радугу
      LL_GPIO_TogglePin(LEDB_GPIO_Port, LEDB_Pin);
  }



  static uint16_t led = 0;
//  static uint16_t lastled = LED_COUNT - 1;


  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
      LL_mDelay(70);


      ws2812_set_rgb(lastled,0,0,0);
      ws2812_set_rgb(led,0,255,255);
      lastled=led;
      led = (led+1)% LED_COUNT;
      ws2812_start();


  	LL_GPIO_TogglePin(LEDB_GPIO_Port, LEDB_Pin);
  }
  /* USER CODE END 3 */
}

